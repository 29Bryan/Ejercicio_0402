    private void actualizarTable1(){
        String [] encabezado = new String[6];
        
        encabezado[0]="Nombre";
        encabezado[1]="Codigo";
        encabezado[2]="Modalidad";
        encabezado[3]="CostoHora";
        encabezado[4]="NumHoras";
        encabezado[5]="Estudiante";
        
        var datos = new Object[this.asignaturaControl.listar().size()][6]; 
        
        var i=0; 
        
        for(var asignatura:this.asignaturaControl.listar()){
            datos[i][0]=asignatura.getNombre(); 
            datos[i][1]=asignatura.getCodigo(); 
            datos[i][2]=asignatura.getModalidad(); 
            datos[i][3]=asignatura.getCostoHora(); 
            datos[i][4]=asignatura.getNumeroHoras(); 
            datos[i][5]=asignatura.getEstudiante().getNombre(); 
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
    }
